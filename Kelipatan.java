public class Kelipatan {
    public static void main(String[] args) {
        double nilaiAwal = 3.5;
        double batasAtas = 35.0;

        // Looping untuk mencetak kelipatan 3.5
        for (double i = nilaiAwal; i <= batasAtas; i += nilaiAwal) {
            System.out.println(i);
        }
    }
}
